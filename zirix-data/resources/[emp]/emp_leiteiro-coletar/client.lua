local Tunnel = module("vrp","lib/Tunnel")
local Proxy = module("vrp","lib/Proxy")
vRP = Proxy.getInterface("vRP")
emP = Tunnel.getInterface("emp_leiteiro-coletar")
-----------------------------------------------------------------------------------------------------------------------------------------
--[ VARIÁVEIS ]--------------------------------------------------------------------------------------------------------------------------
-----------------------------------------------------------------------------------------------------------------------------------------
local processo = false
local segundos = 0
-----------------------------------------------------------------------------------------------------------------------------------------
--[ CORDENADAS DAS VACAS ]---------------------------------------------------------------------------------------------------------------
-----------------------------------------------------------------------------------------------------------------------------------------
local vacas = {
	{ 2432.78,4802.78,34.82 },
	{ 2440.98,4794.38,34.66 },
	{ 2449.0,4786.67,34.65 },
	{ 2457.28,4778.75,34.52 },
	{ 2464.67,4770.23,34.38 }
}

local AnimPos = {
	{ ['x'] = 2433.16, ['y'] = 4802.25, ['z'] = 34.8 },
	{ ['x'] = 2441.39, ['y'] = 4793.88, ['z'] = 34.67 },
	{ ['x'] = 2449.36, ['y'] = 4786.24, ['z'] = 34.64 },
	{ ['x'] = 2457.63, ['y'] = 4778.2, ['z'] = 34.51 },
	{ ['x'] = 2465.0, ['y'] = 4769.66, ['z'] = 34.37 }
}
-----------------------------------------------------------------------------------------------------------------------------------------
--[ SETS ANIM ]--------------------------------------------------------------------------------------------------------------------------
-----------------------------------------------------------------------------------------------------------------------------------------
RegisterNetEvent("vaca:posicao1")
AddEventHandler("vaca:posicao1", function()
	local ped = PlayerPedId()
	SetEntityHeading(ped, 38.5)
	SetEntityCoords(ped, 2441.35, 4793.9, 34.67,false,false,false,false)
end)
-----------------------------------------------------------------------------------------------------------------------------------------
--[ PROCESSO ]---------------------------------------------------------------------------------------------------------------------------
-----------------------------------------------------------------------------------------------------------------------------------------
Citizen.CreateThread(function()
	while true do
		Citizen.Wait(5)
		if not processo then
			for _,func in pairs(vacas) do
				local ped = PlayerPedId()
				local x,y,z = table.unpack(func)
				local distancia = GetDistanceBetweenCoords(GetEntityCoords(ped),x,y,z)
				if distancia <= 30.0 then
					if distancia <= 1.2 then
						drawTxt("PRESSIONE  ~b~E~w~  PARA ORDENHAR A VACA",4,0.5,0.92,0.35,255,255,255,180)
						if IsControlJustPressed(0,38) and emP.checkItem() then

							TriggerEvent('cancelando',true)
							vRP._playAnim(false,{{"amb@medic@standing@tendtodead@base","base"}},true)						
							processo = true
							segundos = 8

							SetTimeout(8000,function()
								emP.checkAddItem()
								vRP._stopAnim(false)
								emP.checkPayment()
							end)
						end	
					end
				end
			end
		end
		if processo then
			drawTxt("AGUARDE ~b~"..segundos.."~w~ SEGUNDOS ATÉ FINALIZAR A EXTRAÇÃO DO LEITE",4,0.5,0.92,0.35,255,255,255,180)
		end
	end
end)
-----------------------------------------------------------------------------------------------------------------------------------------
--[ SEGUNDOS ]---------------------------------------------------------------------------------------------------------------------------
-----------------------------------------------------------------------------------------------------------------------------------------
Citizen.CreateThread(function()
	while true do
		Citizen.Wait(1000)
		if processo then
			if segundos > 0 then
				segundos = segundos - 1
				if segundos == 0 then
					processo = false
					TriggerEvent('cancelando',false)
				end
			end
		end
	end
end)
-----------------------------------------------------------------------------------------------------------------------------------------
--[ FUNÇÕES ]----------------------------------------------------------------------------------------------------------------------------
-----------------------------------------------------------------------------------------------------------------------------------------
function drawTxt(text,font,x,y,scale,r,g,b,a)
	SetTextFont(font)
	SetTextScale(scale,scale)
	SetTextColour(r,g,b,a)
	SetTextOutline()
	SetTextCentre(1)
	SetTextEntry("STRING")
	AddTextComponentString(text)
	DrawText(x,y)
end